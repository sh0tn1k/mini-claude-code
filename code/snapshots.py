"""
Снимки файлов перед перезаписью (undo / revert).

Воплощаем механизм из статьи (тема 14):

  * Каждый write/edit НЕЗАМЕТНО сохраняет прежнее содержимое файла ДО перезаписи.
    Делает это КОД обвязки, а не модель по своей воле, — страховка срабатывает
    всегда, даже если модель поспешила или ошиблась. (Код держит дисциплину,
    смысл — на модели: см. конспект, повторяющийся принцип.)

  * revert возвращает файл к прошлому состоянию одним вызовом. Дешёвая
    обратимость → модель может действовать смело (переписать файл целиком),
    не боясь молчаливо всё сломать.

  * Сам revert — это СТРУКТУРА (тупое восстановление байтов). А РЕШЕНИЕ, когда
    откатывать («это изменение сломало сборку»), — смысл, его принимает модель
    или человек. Снимок не решает — он лишь даёт кнопку.

Проектные решения:
  * Храним СТЕК прежних состояний на каждый путь, а не одну копию: несколько
    последовательных write в один файл откатываются по одному назад (история
    undo, а не только «последнее хорошее»).
  * Маркер _MISSING = «файла не было». Если модель СОЗДАЛА файл, revert вернёт
    его к «не существовал» (удалит), а не оставит пустышку.

Упрощение (осознанное): история живёт в памяти процесса — это undo в пределах
сессии. В реальном Claude Code снимки переживают и рестарт; здесь durability
ради ясности опускаем (ср. task_graph, который на диске).
"""

from __future__ import annotations

from pathlib import Path

# Маркер состояния «такого файла ещё не было» — чтобы отличить его от пустого.
_MISSING = object()

# path (нормализованный) -> стек прежних состояний (str или _MISSING).
_history: dict[str, list] = {}


def _key(path: str) -> str:
    return str(Path(path))


def snapshot(path: str) -> None:
    """Снять снимок ТЕКУЩЕГО состояния файла перед его перезаписью.

    Вызывает КОД инструмента write/edit сам, до записи. Если файла нет — кладём
    маркер _MISSING (значит откат должен будет его удалить)."""
    p = Path(path)
    prev = p.read_text(encoding="utf-8", errors="replace") if p.exists() else _MISSING
    _history.setdefault(_key(path), []).append(prev)


def revert(path: str) -> str:
    """Откатить файл к последнему снимку (снять один шаг со стека)."""
    stack = _history.get(_key(path))
    if not stack:
        return f"ОШИБКА: нет снимка для {path} — откатывать нечего"
    prev = stack.pop()
    p = Path(path)
    if prev is _MISSING:
        # файла до правки не было → возвращаем к «не существовал»
        if p.exists():
            p.unlink()
        return f"OK: {path} возвращён к состоянию «файла не было» (удалён)."
    p.write_text(prev, encoding="utf-8")
    left = len(stack)
    tail = f" (в истории ещё {left} снимок(ов))" if left else " (история этого файла исчерпана)"
    return f"OK: {path} откачен к прошлому состоянию, {len(prev)} символов.{tail}"
