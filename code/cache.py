"""
Наблюдаемость prompt-кэша (тема 20).

ВАЖНО про наш стек. Маркеры cache_control:{"type":"ephemeral"} из статьи —
это Anthropic-only способ вручную указать границу кэшируемого префикса. У нас
DeepSeek (OpenAI-совместимый), и он кэширует контекст АВТОМАТИЧЕСКИ на диске:
размечать блоки не нужно и нельзя. Управляем мы только тем, что реально в наших
руках, — РАСКЛАДКОЙ промпта: стабильное (system + определения инструментов)
идёт первым, летучее (история, результаты тулов) — в хвост. Кэш живёт по
префиксу и переиспользуется ровно до первого различия, поэтому дисциплина
«стабильное вперёд» и есть вся оптимизация; менять начало промпта дорого.

Этот модуль не влияет на кэш — он делает его ВИДИМЫМ. DeepSeek отдаёт в usage
финального чанка:
    prompt_cache_hit_tokens  — сколько токенов префикса обслужено из кэша (~10% цены)
    prompt_cache_miss_tokens — сколько посчитано заново (в т.ч. запись кэша)
Считаем их по каждому вызову и показываем HIT/MISS на ходу и итог в конце —
та же идея наблюдаемости сбоку от горячего пути, что и у хуков (шина событий).
"""

from __future__ import annotations

# Доля цены кэш-хита от обычного токена у DeepSeek ~10% → экономия ~90%.
_SAVED_FRACTION = 0.9


class CacheStats:
    def __init__(self) -> None:
        self.calls = 0
        self.hit = 0        # токены, обслуженные из кэша
        self.miss = 0       # токены, посчитанные заново (вкл. запись кэша)

    def record(self, usage) -> tuple[int, int]:
        """Забрать поля кэша из usage одного ответа. Возвращает (hit, miss)
        этого вызова — чтобы вызывающий мог тут же показать строку хода."""
        hit = getattr(usage, "prompt_cache_hit_tokens", 0) or 0
        miss = getattr(usage, "prompt_cache_miss_tokens", 0) or 0
        self.calls += 1
        self.hit += hit
        self.miss += miss
        return hit, miss

    @staticmethod
    def saved(hit_tokens: int) -> int:
        """Сколько токенов сэкономил кэш-хит (~90% от обслуженных из кэша)."""
        return int(hit_tokens * _SAVED_FRACTION)


# Единый трекер на сессию (как stats в статье).
stats = CacheStats()
