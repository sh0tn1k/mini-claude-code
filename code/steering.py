"""
Steering-очередь h2A — вставка прерываний в реальном времени (тема 19).

Ctrl+C посреди длинной задачи не роняет агента и не выбрасывает сделанное:
он читает прерывание, аккуратно суммирует прогресс и ждёт новой инструкции.
Длинная задача перестаёт быть необратимым обязательством — её можно
перенаправить на середине.

  * ПРОИЗВОДИТЕЛЬ — обработчик Ctrl+C в REPL. Он живёт в отдельном месте от
    цикла (пока цикл крутится в agent_task, REPL ждёт его в `await`). Ctrl+C
    прилетает туда, и REPL кладёт сообщение СЮДА, в канал.
  * ПОТРЕБИТЕЛЬ — сам агентный цикл, и только он. В своих БЕЗОПАСНЫХ точках
    (начало витка и перед запуском инструментов) он вычитывает канал и
    вставляет прерывание в messages как user-сообщение.

Почему канал — очередь, а не флаг и не прямой messages.append из обработчика:
producer и consumer трогают общее состояние (messages) как бы одновременно.
Если Ctrl+C сам сделает append — он может воткнуть сообщение между tool_call и
его tool-ответом → битый контракт для API. Очередь оставляет ЕДИНСТВЕННЫМ
писателем messages сам цикл (это ровно тема 1: у общего состояния один
писатель), а канал несёт ТЕКСТ прерывания, а не просто «стоп». Источников
может быть много (Ctrl+C, таймер, сокет) — все пишут в один канал, цикл
разгребает единообразно.

producer и consumer — обе корутины ОДНОГО event loop, потоки не пересекаются,
поэтому канал — asyncio.Queue (в отличие от background.py, где производитель —
настоящий поток и нужна потокобезопасная queue.Queue).
"""

from __future__ import annotations

import asyncio

# Текст прерывания по Ctrl+C. Несём именно инструкцию модели: остановись,
# суммируй сделанное/оставшееся, жди — а не голый флаг «стоп».
INTERRUPT = (
    "[INTERRUPT] Пользователь нажал Ctrl+C. Немедленно остановись. "
    "Кратко суммируй, что уже сделано, над чем работал и что осталось. "
    "Инструменты сейчас НЕ вызывай — просто отчитайся текстом и жди новой инструкции."
)

# Ленивая инициализация: asyncio.Queue привязывается к текущему циклу. Создаём
# при первом обращении — уже внутри работающего loop (asyncio.run(repl())).
_queue: "asyncio.Queue[str] | None" = None


def _q() -> "asyncio.Queue[str]":
    global _queue
    if _queue is None:
        _queue = asyncio.Queue()
    return _queue


def interrupt(message: str = INTERRUPT) -> None:
    """ПРОИЗВОДИТЕЛЬ: положить прерывание в канал. messages НЕ трогаем — это
    сделает сам цикл в безопасной точке. Очередь без границы — put_nowait не
    блокирует и не требует await."""
    _q().put_nowait(message)


def drain() -> list[str]:
    """ПОТРЕБИТЕЛЬ (сам цикл, в безопасной точке): забрать все накопленные
    прерывания, не блокируя."""
    out: list[str] = []
    q = _q()
    while True:
        try:
            out.append(q.get_nowait())
        except asyncio.QueueEmpty:
            break
    return out


def has_pending() -> bool:
    """Есть ли непрочитанные прерывания в канале."""
    return not _q().empty()
